int	ft_str_is_alpha(char *str)
{
	int	i;
	int	cnt;

	i = 0;
	cnt = 0;
	while (str[i])
	{
		if ((str[i] >= 'A' && str[i] <= 'Z')
			|| (str[i] >= 'a' && str[i] <= 'z'))
				cnt++;
		i++;
	}
	if (i == cnt || str[0] == '\0')
		return (1);
	else
		return (0);
}
